package com.intuit.accountant.services.dcm.exceptions;

public class DCMServiceException extends RuntimeException {
	private static final long serialVersionUID = 2840426719596803147L;

	public DCMServiceException() {
		super();
	}

	public DCMServiceException(String message) {
		super(message);
	}

	public DCMServiceException(Throwable cause) {
		super(cause);
	}

	public DCMServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	public DCMServiceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
}
